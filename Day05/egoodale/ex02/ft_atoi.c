int		ft_atoi(char *str)
{
	int sign;
	int num;

	sign = 1;
	num = 0;
	while (*str < 33)
		str++;
	if (*str == '+')
	{
		str++;
		if (!(*str >= '0' && *str <= '9'))
			return (0);
	}
	if (*str == '-')
	{
		sign = -1;
		str++;
	}
	while ((*str <= '9' && *str >= '0'))
	{
		num = num * 10 + *str - '0';
		str++;
	}
	return (sign * num);
}
